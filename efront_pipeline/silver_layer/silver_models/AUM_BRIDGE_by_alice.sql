-- DBTITLE 1,PORTFOLIO_LEVEL
SELECT 
  FUND_ID
  , FUND
  , PORTFOLIO_ID
  , PORTFOLIO
  , REPORT_DATE
  , COALESCE(LAG(CLOSING_AUM) OVER (PARTITION BY FUND_ID, PORTFOLIO_ID ORDER BY REPORT_DATE), 0) AS OPENING_AUM
  , COALESCE(CAPITAL_INJ, 0) AS CAPITAL_INJ
  , CLOSING_AUM 
      - COALESCE(LAG(CLOSING_AUM) OVER (PARTITION BY FUND_ID, PORTFOLIO_ID ORDER BY REPORT_DATE), 0) 
      - COALESCE(CAPITAL_INJ, 0) 
      - (ASSET_COMMITTED_EQUITY - COALESCE(LAG(ASSET_COMMITTED_EQUITY) OVER (PARTITION BY FUND_ID, PORTFOLIO_ID ORDER BY REPORT_DATE), 0))
      - (ASSET_EXTERNAL_DEBT - COALESCE(LAG(ASSET_EXTERNAL_DEBT) OVER (PARTITION BY FUND_ID, PORTFOLIO_ID ORDER BY REPORT_DATE), 0))
      - COALESCE(DISPOSAL, 0) 
      - (FUND_LEVEL_ADJ - COALESCE(LAG(FUND_LEVEL_ADJ) OVER (PARTITION BY FUND_ID, PORTFOLIO_ID ORDER BY REPORT_DATE), 0))
      AS NAV_CHANGE
  , ASSET_COMMITTED_EQUITY - COALESCE(LAG(ASSET_COMMITTED_EQUITY) OVER (PARTITION BY FUND_ID, PORTFOLIO_ID ORDER BY REPORT_DATE), 0) AS ASSET_COMMITTED_EQUITY
  , ASSET_EXTERNAL_DEBT - COALESCE(LAG(ASSET_EXTERNAL_DEBT) OVER (PARTITION BY FUND_ID, PORTFOLIO_ID ORDER BY REPORT_DATE), 0) AS ASSET_EXTERNAL_DEBT
  , COALESCE(DISPOSAL, 0) AS DISPOSAL
  , FUND_LEVEL_ADJ - COALESCE(LAG(FUND_LEVEL_ADJ) OVER (PARTITION BY FUND_ID, PORTFOLIO_ID ORDER BY REPORT_DATE), 0) AS FUND_LEVEL_ADJ
  , CLOSING_AUM
FROM AUM_BRIDGE_BASE
JOIN (SELECT DISTINCT FUND_ID, FUND, PORTFOLIO_ID, PORTFOLIO FROM REGISTER) USING (FUND_ID, PORTFOLIO_ID)
WHERE AUM_BRIDGE_BASE.PORTFOLIO_ID != 'n/a_Undeployed' --NOT DISPLAYING UNDEPLOYED AS THEY ARE FUND LEVEL ADJUSTMENTS
ORDER BY REPORT_DATE, FUND, PORTFOLIO

