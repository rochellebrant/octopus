-- DBTITLE 1,FUND_LEVEL
SELECT 
  FUND_ID
  , FUND
  , REPORT_DATE
  , COALESCE(LAG(SUM(CLOSING_AUM)) OVER (PARTITION BY FUND_ID ORDER BY REPORT_DATE), 0) AS OPENING_AUM
  , SUM(CAPITAL_INJ) AS CAPITAL_INJ
  , SUM(CLOSING_AUM) 
      - COALESCE(LAG(SUM(CLOSING_AUM)) OVER (PARTITION BY FUND_ID ORDER BY REPORT_DATE), 0) 
      - SUM(CAPITAL_INJ) 
      - (SUM(ASSET_COMMITTED_EQUITY) - COALESCE(LAG(SUM(ASSET_COMMITTED_EQUITY)) OVER (PARTITION BY FUND_ID ORDER BY REPORT_DATE), 0))
      - (SUM(ASSET_EXTERNAL_DEBT) - COALESCE(LAG(SUM(ASSET_EXTERNAL_DEBT)) OVER (PARTITION BY FUND_ID ORDER BY REPORT_DATE), 0))
      - SUM(DISPOSAL) 
      - (SUM(FUND_LEVEL_ADJ) - COALESCE(LAG(SUM(FUND_LEVEL_ADJ)) OVER (PARTITION BY FUND_ID ORDER BY REPORT_DATE), 0))
      AS NAV_CHANGE
  , SUM(ASSET_COMMITTED_EQUITY) - COALESCE(LAG(SUM(ASSET_COMMITTED_EQUITY)) OVER (PARTITION BY FUND_ID ORDER BY REPORT_DATE), 0) AS ASSET_COMMITTED_EQUITY
  , SUM(ASSET_EXTERNAL_DEBT) - COALESCE(LAG(SUM(ASSET_EXTERNAL_DEBT)) OVER (PARTITION BY FUND_ID ORDER BY REPORT_DATE), 0) AS ASSET_EXTERNAL_DEBT
  , SUM(DISPOSAL) AS DISPOSAL
  , SUM(FUND_LEVEL_ADJ) - COALESCE(LAG(SUM(FUND_LEVEL_ADJ)) OVER (PARTITION BY FUND_ID ORDER BY REPORT_DATE), 0) AS FUND_LEVEL_ADJ
  , SUM(CLOSING_AUM) AS CLOSING_AUM
FROM AUM_BRIDGE_BASE
JOIN (SELECT DISTINCT FUND_ID, FUND FROM REGISTER) USING (FUND_ID)
GROUP BY ALL
ORDER BY REPORT_DATE, FUND

